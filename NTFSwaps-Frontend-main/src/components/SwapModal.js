const useStyles = makeStyles((theme) => ({
  modal: {
    display: "flex",
    alignItems: "center",
    justifyContent: "center",
  },
  paper: {
    backgroundColor: theme.palette.background.paper,
    border: "0px solid #000",
    borderRadius: "10px",
    boxShadow: theme.shadows[5],
    padding: theme.spacing(2, 4, 3),
    maxWidth: "600px",
    margin: "0 10px",
  },
  root: {
    width: "100%",
  },
}));

export default function SwapModal(props) {
  const classes = useStyles();
  const [nftId, setNftId] = useState("");

  const mintTokens = async () => {
    var contract = new props.web3.eth.Contract(
      props.abi,
      props.contractAddress
    );
    var nftContract = new props.web3.eth.Contract(nftAbi, props.address);
    var approval = await nftContract.methods
      .isApprovedForAll(props.coinbase, props.contractAddress)
      .call();
    console.log(approval);
    if (!approval) {
      nftContract.methods
        .setApprovalForAll(props.contractAddress, true)
        .send({ from: props.coinbase });
      props.openSnack("error", "Please Await Approval And Try Again");
      props.handleClose();
      return;
    }
    contract.methods
      .mintToken(props.address, nftId.split(" "))
      .send({ from: props.coinbase })
      .then((e) => {
        props.openSnack("success", "Tokens Minted");
        props.getBalance();
      })
      .catch((e) => {
        props.openSnack("error", "Please Await Approval And Try Again");
      });
    props.handleClose();
  };

  return (
    <Modal
      aria-labelledby="transition-modal-title"
      aria-describedby="transition-modal-description"
      className={classes.modal}
      open={props.open}
      onClose={props.handleClose}
      closeAfterTransition
      BackdropComponent={Backdrop}
      BackdropProps={{
        timeout: 500,
      }}
    >
      <Fade in={props.open}>
        <div className={classes.paper}>
          <h2>Deposit NFT</h2>
          <p>
            Please enter the NFT or NFTs you want to deposit seperated by space.
          </p>
          <TextField
            className={classes.root}
            id="outlined-basic"
            color="primary"
            variant="outlined"
            onChange={(e) => setNftId(e.target.value)}
            InputProps={{ className: classes.input }}
            placeholder="NFT IDs"
          />
          <br />
          <br />
          <br />
          <br />
          <div style={{ textAlign: "center" }}>
            <FilledWideButton onClick={mintTokens}>Mint</FilledWideButton>
          </div>
        </div>
      </Fade>
    </Modal>
  );
}
